function reverseArray(arr) {
  return arr.reverse();
}

console.log(reverseArray([1, 2, 3, 4, 5])); // Output: [5, 4, 3, 2, 1]
console.log(reverseArray(["a", "b", "c"])); // Output: ["c", "b", "a"]

/**
 ** Explanation:
 * reverse() adalah metode bawaan array yang mengubah urutan elemen menjadi kebalikan.
 */