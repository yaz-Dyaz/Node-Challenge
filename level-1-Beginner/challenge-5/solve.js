function greet(name) {
  return `Hello, ${name}!`;
}

console.log(greet("Alice"));
console.log(greet("Bob"));
console.log(greet("Charlie"));


//* Penjelasan:
//* Template literal(${}) memudahkan untuk menyisipkan variabel ke dalam string.
//* Fungsi greet dirancang agar dapat digunakan kembali dengan parameter yang berbeda.